package day03_escape_sequence;

public class EscapeSequence {
    public static void main(String[] args){
        System.out.println("I love \"JAVA\" programming");
        System.out.println("Lets buy \"coffee\" and \"tea\" ");
        System.out.println("\\");
        System.out.println("java\nintellij");
        System.out.println("\tjava\tselenium\tapi");
        System.out.println("java\"");
        System.out.println("java\'");
        System.out.println("java\\");
        System.out.println("I love \"Java programming\"");
        System.out.println("My name is \"AzIZ\"");
        System.out.println("lets buy \'coffee\' and \'tea\'");
        System.out.println("Today, I had an appointment with Mr John; \n When we met, he was happy.");
        System.out.println();

    }
}
