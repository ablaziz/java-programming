package day03_escape_sequence;

public class EscapeSequence2 {
    public static void main(String[] args){
        System.out.println("\"Iphone case\", \"Iphone\", \n\"Xbox\", \t\t\"Monitor\", \t\"Webcam\".");
        System.out.println("   ^   ");
        System.out.println(" /    \\");
        System.out.println(" |     |");
        System.out.println("-------- ");
        System.out.println("\"AZIZ\"");
        System.out.println("\'NIADA\'");
        System.out.println("\\AZIZ\\ \\NIADA\\");

        System.out.println("   ^   ");
        System.out.println(" /   \\");
        System.out.println(" |    |");
        System.out.println(" |    |");
        System.out.println("========");
    }

}
