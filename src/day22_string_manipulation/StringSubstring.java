package day22_string_manipulation;

public class StringSubstring {
    public static void main(String[] args) {
        String word = "courage is power";
        System.out.println(word.substring(0,9));
        System.out.println(word.substring(3,15));
        System.out.println(word.substring(9));
        System.out.println(word.substring(8,10));
        System.out.println(word.substring(10));
    }
}
